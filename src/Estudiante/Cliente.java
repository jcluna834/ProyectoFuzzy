    /**
     * Carga cierta cantidad de imagenes de manera aleatoria en la lista de
     * imagenes
     *
     * @param cantidad
     */
    private void cargarImagenes(int cantidad) {
        int i = 0;
        imagenes = new ArrayList<>();
        while (i < cantidad) {
            String palabra = servidor.getPalabraAletoria();
            if (!palabra.isEmpty()) {
                String[] aux = palabra.split(";");
                int codigo = Integer.parseInt(aux[0]);
                if (!existeImagen(codigo)) {
                    imagenes.add(new Imagen("imagenes/" + aux[1] + ".jpg", codigo));
                    i++;
                }
            } else {
                JOptionPane.showMessageDialog(null, "Error!! no hay palabras en los diccionarios");
                break;
            }
        }
    }

    /**
     * Valida si una imagen ya existe en la lista de imagenes
     *
     * @param codigo a ser buscado en la lista de imagenes
     * @return true si el codigo ingresado coincide con alguno de la lista de
     * imagenes, false en caso de no encontrarlo
     */
    private boolean existeImagen(int codigo) {
        boolean existe = false;
        for (Imagen i : imagenes) {
            if (!existe) {
                if (i.getCodigo() == codigo) {
                    existe = true;
                }
            } else {
                break;
            }
        }
        return existe;
    }

