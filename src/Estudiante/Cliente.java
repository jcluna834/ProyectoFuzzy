    private int jugar(double[] data) {
        double dificultad = data[0];
        if (dificultad >= 0 && dificultad < 35) {
            return 1;
        } else {
            if (dificultad >= 35 && dificultad < 60) {
                return 2;
            } else {
                return 3;
            }
        }
    }

