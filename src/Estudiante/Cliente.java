    private void validar() {
        if (!terceraFase) {
            if (!segundaFase) {
                if (jTextFieldJuego3Pal1.getText().equals(jTextField3Pal1.getText()) || jTextFieldJuego3Pal1.getText().equals(jTextField3Pal2.getText())) {
                    if (!validar1 && !segundaFase) {
                        validar1 = true;
                    } else {
                        segundaFase = true;
                        validar1 = false;
                        validar2 = false;
                        validar3 = false;
                        validar4 = false;
                        validar5 = false;
                        correctoJuego3(6);
                    }
                } else {
                    jTextFieldOpc1MsjJuego3.setText("Upppssss :(");
                    equivocaciones += 4;
                    iniciarNuevoJuego();
                }
            } else if (segundaFase && !terceraFase && (jTextFieldJuego3Pal1.getText().equals(jTextField3Pal1.getText()) || jTextFieldJuego3Pal1.getText().equals(jTextField3Pal2.getText()) || jTextFieldJuego3Pal1.getText().equals(jTextField3Pal3.getText()))) {
                if (!validar1 && !terceraFase) {
                    validar1 = true;
                } else if (!validar2 && !terceraFase) {
                    validar2 = true;
                } else {
                    terceraFase = true;
                    validar1 = false;
                    validar2 = false;
                    validar3 = false;
                    validar4 = false;
                    validar5 = false;
                    correctoJuego3(6);
                }
            } else {
                jTextFieldOpc1MsjJuego3.setText("Upppssss :(");
                equivocaciones += 4;
                iniciarNuevoJuego();
            }
        } else if (terceraFase && (jTextFieldJuego3Pal1.getText().equals(jTextField3Pal1.getText()) || jTextFieldJuego3Pal1.getText().equals(jTextField3Pal2.getText()) || jTextFieldJuego3Pal1.getText().equals(jTextField3Pal3.getText()) || jTextFieldJuego3Pal1.getText().equals(jTextField3Pal4.getText()))) {
            if (!validar1) {
                validar1 = true;
            } else if (!validar2) {
                validar2 = true;
            } else if (!validar3) {
                validar3 = true;
            } else {
                validar1 = false;
                validar2 = false;
                validar3 = false;
                validar4 = false;
                validar5 = false;
                correctoJuego3(6);
            }
        } else {
            jTextFieldOpc1MsjJuego3.setText("Upppssss :(");
            equivocaciones += 4;
            iniciarNuevoJuego();
        }
    }

